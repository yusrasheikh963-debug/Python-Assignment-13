import numpy as np

arr1 = np.zeros(8)   #1D array of 8 zeros
arr2 = np.ones((4, 4))   #2D array of shape (4, 4) filled with ones

matrix_3x3 = np.zeros((3, 3))   #3x3 matrix of zeros

print("1D array of 8 zeros:")   # Print the 1D array
print(arr1)

print("\n4*4 array of ones:")   #print the 2D array
print(arr2)

print("\n3*3 matrix of zeros:")   #print the 3x3 matrix
print(matrix_3x3)